# Conceptos o Términos de la POO:
#################################
# -Clase: Modelo donde se redactan las características comunes de un grupo de objetos.
# -Instancia de Clase: Objeto o Ejemplar perteneciente a una clase.
# -Modularización: Programas divididos en trozos, partes, módulos o clases.
# -Encapsulamiento: Funcionamento interno de la clase sea accesible solo desde adentro de su clase.
# -Métodos de Acceso: Conectamos unas clases con otras para que funcionen como una unidad o un equipo.
# -Nomenclatura del Punto: Acceder a las propiedades y comportamientos de un objeto (pseudocódigo).
# ej: acceder a las propiedades -> miCoche.color="rojo"
#     acceder a un comportamiento -> miCoche.arranca()

